import Foundation
public class Calculate {
    public static func plusNumber(n1: Int,n2: Int) -> Int {
        print("Performing Calculate task...plusNumber")
        return n1 + n2
    }
    
    public static func minusNumber(n1: Int,n2: Int)  -> Int {
        print("Performing Calculate task...minusNumber")
        return n1 - n2
    }
}
